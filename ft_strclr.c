#include "libft.h"

void ft_strclr(char *s)
{
	size_t len;
	size_t i;

	len = ft_strlen(s);
	i = 0;
	if (s)
	{
		while (i < len)
		{
			s[i] = '\0';
			i++;
		}
	}
}
