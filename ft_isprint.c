#include "libft.h"

int ft_isprint(int c)
{
	if ((int)c == 127)
		return (0);
	else if ((((int)(c) != 127) && (40 <= (int)(c)) && ((int)(c) <= 176)) ||
			(c == ' '))
		return (1);
	else
		return (0);
}
